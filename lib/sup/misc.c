char           *
strnalloc(const char *str, int len)
{
	char           *buf;

	if (str == NULL)
		return (NULL);
	if (len == 0)
		len = strlen(str);
	buf = calloc(1, len + 1);
	if (buf != NULL)
		memcpy(buf, str, len);

	return (buf);
}

int
strrealloc(char **dst, const char *src)
{
	char           *old, *tmp;

	if (dst == NULL)
		return (-1);

	old = *dst;

	tmp = strdup(src);
	if (tmp == NULL)
		return (-1);

	*dst = tmp;

	if (old != NULL)
		free(old);

	return (0);
}

char           *
next_token(char **ptr, const char *delim)
{
	char           *str;

	do {
		str = strsep(ptr, delim);
	} while (str != NULL && *str == '\0');

	return (str);
}

/*
 * synopsis:
 *	converts char * value to long
 * entry:
 *	long *val: pointer to result
 *	char *str: string to be converted
 * returns:
 *	-1 on invalid string, -2 on range error
 *	0 on success
 */

int
str2long(long *val, const char *str, int base)
{
	char           *ep;

	errno = 0;
	*val = strtol(str, &ep, base);

	if (str[0] == '\0' || *ep != '\0')
		return (-1);

	if (errno == ERANGE && (*val == LONG_MAX || *val == LONG_MIN))
		return (-2);

	return (0);
}

/*
 * synopsis:
 *	converts char * value to unsigned long
 * entry:
 *	unsigned long *val: pointer to result
 *	char *str:	    string to be converted
 * returns:
 *	-1 on invalid string, -2 on range error
 *	0 on success
 */
int
str2ulong(unsigned long *val, const char *str, int base)
{
	char           *ep;

	errno = 0;
	*val = strtoul(str, &ep, base);

	if (str[0] == '\0' || *ep != '\0')
		return (-1);

	if (errno == ERANGE && *val == ULONG_MAX)
		return (-2);

	return (0);
}

/*
 * synopsis:
 *	converts char * value to u_int64_t
 * entry:
 *	u_int64_t *val: pointer to result
 *	char *str:	    string to be converted
 * returns:
 *	-1 on invalid string, -2 on range error
 *	0 on success
 */
int
str264(u_int64_t * val, const char *str, int base)
{
	char           *ep;

	*val = strtoull(str, &ep, base);
	if (str[0] == '\0' || *ep != '\0')
		return (-1);
	if (errno == ERANGE && (*val == (__extension__ ULLONG_MAX) ||
				*val == 0))
		return (-2);
	return (0);
}

void
rtrim(char **str)
{
	int             i;

	i = strlen(*str) - 1;
	if (i > 0) {
		while (i >= 0 && isspace((*str)[i--]));
		(*str)[i + 2] = '\0';
	}
}

void
ltrim(char **str)
{
	if (*str != NULL)
		while (isspace((*str)[0]))
			++* str;
}

void
strupper(char *str)
{
	if (str != NULL)
		for (; *str != '\0'; *str = koi8_2up[(u_int8_t)*str], ++str);
}

void
strlower(char *str)
{
	if (str != NULL)
		for (; *str != '\0'; *str = koi8_2low[(u_int8_t)*str], ++str);
}

int
get_string_index(char *const table[], const char *str)
{
	int             i;

	for (i = 0; table[i] != NULL; ++i)
		if (!strcmp(table[i], str))
			return (i);
	return (-1);
}

int
check_ymd(int y, int m, int d)
{
	/* days per month */
	u_int8_t        dpm[12] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};

	if (y < 1583 || m < 1 || m > 12 || d < 1)
		return (0);

	if (ISLEAP(y))
		dpm[1] = 29;

	if (d > dpm[m - 1])
		return (0);

	return (1);
}

int
check_hms(int h, int m, int s)
{
	return (h >= 0 && h < 23 && m >= 0 && m < 60 && s >= 0 && s < 60);
}

int
find_field(char * match, char * path, int field_no, char **rv)
{
	FILE           *fp;
	char            buf[1024];
	int             i;
	int		j;
	int		rc;
	char	       *p;
	char           *fmatch;	
	char	       *field;

	*rv = NULL;
	rc = -1;
	
	fp = fopen(path, "r");
	if (fp == NULL)
		return (errno);

	i = 0;
	field = NULL;

	/* find field */
	for (;;) {
		memset(buf, 0, sizeof buf);
		if (fgets(buf, sizeof(buf), fp) == NULL)
			break;
		++i;
		p = strchr(buf, '\n');
		if (p == NULL)
			/* skip long line and */
			continue;

		/* replace '\n' */
		*p = '\0';

		/* skip comment */
		p = strchr(buf, '#');
		if (p != NULL)
			*p = '\0';

		/* load match */
		p = buf;
		fmatch = next_token(&p, "\t ");
		if (fmatch == NULL || strcmp(match, fmatch))
			continue;

		/* load password hash */
		j = 1;
		do {
			field = next_token(&p, "\t ");
			++j;
		} while(field != NULL && j < field_no);
		if (field == NULL)
			continue;
		rc = 0;	
		break;
	}
	fclose(fp);
	*rv = field;
	return (rc);
}

/* finds countinuous chain of symbols */
int
find_chain(char * start, int len, char ** s, char ** e, int (*f)(int))
{
	int i;

	if (start == NULL)
		return (-1);

	if (len == -1)
		len = strlen(start);

	*s = *e = NULL;

	for (i = 0; i <= len; ++i) {
		if (f(start[i]))
			*e = *s == NULL	? *s = &start[i] : &start[i];
		else
			if (*s != NULL)
				break;
	}
	
	if (*s == NULL || *e == NULL)
		return (-1);
	++*e;

	return (0);
}
